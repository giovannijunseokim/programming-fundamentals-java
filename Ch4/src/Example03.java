public class Example03 {
    public static void main(String[] args) {
        int num = 20;

        if (num == 10) {
            System.out.println("10");
        } else if (num == 15) {
            System.out.println("15");
        } else if (num == 20) {
            System.out.println("20");
        } else {
            System.out.println("Not a number");
        }
    }
}